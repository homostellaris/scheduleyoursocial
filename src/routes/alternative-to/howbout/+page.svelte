<h2>
  Schedule Your Social is similar to other social planning apps like Howbout
</h2>
<p>
  However we solve a slightly different use case to Howbout and differ in a few
  key ways.
</p>
<ul>
  <li>
    Schedule Your Social aims to be as simple and quick to use as possible in
    contrast to Howbout which was more features but might take longer to arrive
    at a time for your social.
  </li>
  <li>
    Schedule Your Social only allows you to agree a date to meet, not a time.
  </li>
  <li>
    Schedule Your Social provides real-time updates so you don't need to refresh
    the page!
  </li>
  <li>
    Schedule Your Social is not a mobile app, its a web app, but it still works
    great on mobile.
  </li>
</ul>
